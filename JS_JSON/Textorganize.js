console.log(JSON.parse('"Hello World"'));
console.log(JSON.parse("3"));
console.log(JSON.parse("true"));
console.log(JSON.parse("null"));
console.log(JSON.parse('{ "a": 1, "b": 2 }'));
console.log(JSON.parse("[1, 2, 3]"));
// "Hello World"
// 3
// true
// null
// {"a":1,"b":2}
// [1,2,3]
// 각 data type 에 맞게 JSON 해석한다
